    private void generarPaneles(int numTemas) {
        panelMateria.removeAll();
        gridLayout.setColumns(1);
        gridLayout.setRows(numTemas);
        for (int i = 0; i < numTemas; i++) {
            JPanel panel = new JPanel();
            panel.setSize(
                panelMateria.getSize().width - 10,
                60
            );
            panel.setLayout(new FlowLayout());
            
            JLabel label = new JLabel("Tema " + (i+1));
            panel.add(label);
            
            JTextField field = new JTextField(15);
            panel.add(field);
            
            JButton btn = new JButton("ðŸ’¾");
            btn.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e) {
                    materia.setNombreMateria(
                        jTextField1.getText()
                    );

                    materia.setObjectivoMateria(
                        jTextArea1.getText()
                    );

                    Tema t = new Tema();
                    t.setTituloTema(field.getText());
                    t.setNumTema(listTemas.size()+1);
                    listTemas.add(t);
                    
                    materia.setTemas(listTemas);
                    
                    new CapturadorTemaFrame(
                        materia, listTemas.size() - 1
                    ).setVisible(true);
                    
                    btn.setEnabled(false);
                }
            });
            panel.add(btn);
            
            panelMateria.add(panel);
        }
        panelMateria.updateUI();
    }
    
