    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        int numTemas = Integer.parseInt(
            "" + jSpinner1.getValue()
        );
        if(numTemas > 0) {
            generarPaneles(numTemas);
        }
    }//GEN-LAST:event_jSpinner1StateChanged

    private void buttonAction1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAction1ActionPerformed
        if(jTextArea1.getText().isEmpty() || jTextField1.getText().isEmpty()){
            labelNota.setVisible(true);
            return;
        }
        System.out.println(materia.toString());
        for(Tema t : materia.getTemas()) {
            System.out.println("\t" + t.toString());
            for(Subtema s : t.getSubtemas()) {
                System.out.println("\t\t" + s.toString());
            }
        }
        
        try {
            boolean correcto = materia.getTemas().get(0).getSubtemas().size() > 0;
            
            if(correcto) {
                // Guardar el json
                listMaterias.add(materia);
                
                String json = new Gson().toJson(listMaterias);
                
                BufferedWriter bw = new BufferedWriter(
                    new FileWriter("materias_temarios.json", false)
                );
                bw.write(json);
                bw.close();
                
                JOptionPane.showMessageDialog(null,"Guardado");
                dispose();
            } else {
                JOptionPane.showMessageDialog(
                    null, labelNota.getText()
                );
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(
                null, "Error al guardar. Capture toda la info"
            );
        }

    }//GEN-LAST:event_buttonAction1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            BufferedReader br = new BufferedReader(
                new FileReader("materias_temarios.json")
            );
            String lectura;
            String resultado = "";
            while((lectura = br.readLine()) != null) {
                resultado += lectura;
            }
            br.close();
            
            listMaterias.clear();
            JSONParser jParser = new JSONParser();
            JSONArray jArray = (JSONArray) jParser.parse(resultado);
            for (int i = 0; i < jArray.size(); i++) {
                String json = "" + jArray.get(i);
                
                Materia m = new Gson().fromJson(json, Materia.class);
                listMaterias.add(m);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

