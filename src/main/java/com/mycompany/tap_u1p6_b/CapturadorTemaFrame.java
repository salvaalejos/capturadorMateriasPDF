    private void btnMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMenosActionPerformed
        if(numSubtemas > 0) {
            numSubtemas--;
            labelSubtemas.setText("" + numSubtemas);
            
            generarPanels();
        }
    }//GEN-LAST:event_btnMenosActionPerformed

    private void btnMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasActionPerformed
        numSubtemas++;
        labelSubtemas.setText("" + numSubtemas);
        
        generarPanels();
    }//GEN-LAST:event_btnMasActionPerformed

    private void generarPanels()
    {
        listSubtemas.clear();
        panelSubtemas.removeAll();
        
        gridLayout.setColumns(1);
        gridLayout.setRows(numSubtemas);
        for (int i = 0; i < numSubtemas; i++) {
            Subtema s = new Subtema();
            s.setNumSubtema(i+1);
            listSubtemas.add(s);
            
            CapturadorSubtemasPanel panel = new CapturadorSubtemasPanel(
                materia.getTemas().get(indice),
                s
            );
            panelSubtemas.add(panel);
        }
        panelSubtemas.updateUI();
        materia.getTemas().get(indice)
            .setSubtemas(listSubtemas);
    }
    
